			<p>With almost one in three farmers over the normal retirement age but only one in twenty farmers under the age of 35,
							the Department of Agriculture, Food and the Marine launched the Succession Farm Partnership initiative in 2017, to try and change
							the apparent reluctance to legally transfer lands to the next generation.<br><br>

							<strong>What are the objectives?</strong><br><br>

							The main aim is to encourage the transfer of farm assets while also providing security for the transferors, by allowing
							them to retain ownership of up to 20% of their farm assets.<br>

							The initiative allows for young farmers to become involved in the farm business with their parents and to integrate them into
							the management side of the farm business at an earlier stage.In order for farms to remain competitive, the partnership also 
							provides an opportunity to increase scale, maintain security of labour better work-life balance and enhanced safety on-farm. 
							It will also reduce rural isolation and improve the social demographics of Irish farms.<br><br>

							<strong> Benefits of joining a Succession Farm Partnership?</strong><br><br>


							• Define succession and inheritance plan: Under this initiative, both generations are aware of the succession partnership allows
							the successor to integrate themselves into the farm business with their parents.<br>

							• The partnership gives them responsibility and decision making powers and is an important step in the development of the young
							person as a farmer. It provides an ideal transition of the farm business from parent to child.<br>

							• All parties remain involved in the day-to-day running of the enterprise. Responsibilities are shared as outlined in the 
							partnership agreement. As the young farmer now has a share really get involved, take responsibility and develop the farm business.<br><br>

							<strong><i>Work-life balance </i></strong><br><br>

							Improved work-life balance can be achieved when there is additional labour, skills, new enthusiasm and expertise available on
							the farm. Workload can be scheduled and managed, and a rota completed so that all partners enjoy time off and can plan holidays 
							safe in the knowledge that the other partner is familiar with the running of the farm. The enhanced range of skills makes
							the partnership more capable and can lead to better decision making, as well as more timely and safer work practices. Any investments
							required can be shared to reduce the burden on individual partners.<br><br>
							
							<strong>Financial benefits </strong><br>
							Financial incentives include taxation benefits and Common Agricultural Policy (CAP) scheme benefits.<br><br>
							

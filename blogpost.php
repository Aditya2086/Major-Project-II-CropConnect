          <div class="card-description">
			
									<p>
						
							With increasing population, urbanization and contagious depletion of natural resources, there has to 
							be a paradigm shift in farmer’s perception from production to productivity and to profitability. 
							In this present scenario, the major challenge arising are shrinking land and depleting water and other
							related resources in agriculture.<br>

							There is need for promoting farmer friendly location specific production system management technologies
							in a concerted manner to achieve vertical growth in horticulture production dully ensuring quality of 
							produce and better remuneration per unit of area with judicious use of natural resources.<br>

							In this endeavor, precision farming aims to have efficient utilization of resources per unit of time 
							and area for achieving targeted production of horticultural produce.<br><br>

							<strong>What is precision farming?</strong><br>

							Precision farming is a comprehensive information based farm management system to identify, analyze and
							manage variability within fields for optimum profitability, sustainability and protection of land resources.<br>

							In other words, we can say that, it is an efficient management of resources through location specific high 
							tech interventions which includes fertilization, protected/ greenhouse cultivation, soil and leaf nutrient
							based fertilizer management, mulching for in-situ moisture conservation, micro-propagation, high density 
							planting, drip irrigation etc.<br>

							Precision farming integrates environmental health, economic profitability and social and economic equity by
							giving emphasis on crop management using technologies like geographic information system (GIS),
							Global Positioning System (GPS), remote sensing (RS) along with ground equipment like variable rate applicators (VRA),
							yield monitors and computers along with appropriate software.<br>

							Many technological developments including Information and communications technology and Geo-informatics led
							to the development of the concept of precision farming. Its success relies on the integration of these 
							technologies into a single system that can be operated at farm level with sustainable effort. These technological
							developments are as follows:<br><br>

							<strong><i>Geo-informatics</i></strong><br>

							Geo-informatics is a term that appears to have been independently coined by several groups around the world to
							describe a variety of efforts to promote collaboration between computer science and the geosciences to solve 
							complex scientific questions. It is the science and technology of gathering, analyzing, interpreting, distributing
							and using geographic information.<br>

							Geo-informatics otherwise called as geomatics encompasses a broad range of disciplines including surveying 
							and mapping, RS, GIS, and the GPS.<br><br>

							<strong><i>GPS</i></strong><br>

							The GPS is a satellite-based navigation system that can be used to locate positions anywhere on the earth. 
							GPS provides continuous (24 hours/day), real-time, three-dimensional positioning, navigation and timing 
							worldwide in any weather condition.<br>

							GPS was originally intended for military applications, but in the 1980s, the government made the system 
							available for civilian use. There are no subscription fees or setup charges to use GPS. Any person with a
							GPS receiver can access the system, and it can be used for any application that requires location coordinates.<br>

							The development of the publicly available global positioning system (GPS) has opened new doors in opportunities
							for spatial data. More recently farmers have gained access to site specific technology though GPS which is 
							used for yield mapping and variable rate fertilizer/pesticide applicator.<br><br>
							
							<strong> <i>RS Technique</i></strong><br>

							RS is the science of making inferences about material objects from measurements, made at distance, without coming
							into physical contact with the objects under study.<br>

							The RS system consists of a sensor to collect the radiation and a platform — an aircraft, balloon, rocket, 
							satellite or even a ground-based sensor-supporting stand — on which a sensor can be mounted.<br>

							Currently a number of aircraft and spacecraft imaging systems are operating using remote sensing sensors. Some of 
							the current image systems from spacecraft platform include Indian Remote Sensing Satellites (IRS), French National 
							Earth Observation Satellite (SPOT), IKONOS, a commercial Earth observation satellite, etc.<br><br>

							<strong><i>GIS</i></strong><br>

							The GIS is a computerized data storage and retrieval system, which can be used to manage and analyze spatial 
							data relating crop productivity and agronomic factors. It can integrate all types of information and interface 
							with other decision support tools.<br>

							GIS can display analyzed information in maps that allow better understanding of interactions among yield, fertility,
							pests, weeds and other factors, and decision-making based on such spatial relationships.<br><br>
							
								<h3>Related Stories</h3><br>
								
								~ Cutting antimicrobial use, promoting good farming practices can enhance farm productivity<br>
								~ Africa needs climate-smart agriculture: report <br>
								~ E-agriculture can drive rural development, boost agriculture<br>
								~ Now an app to help farmers practice precision agriculture<br>
								~ Many types of GIS software with varying functionality and price are now available. Many farm 
								information systems (FIS) are available, which use simple programs to create a farm level database.<br>
							<br>	

							One example of such FIS is local resources information system, which consists of several modules and enables data import;
							generation of raster files (or bitmap image) by different gridding methods; the storage of raster information in a database;
							the generation of digital agro-resource maps; the creation of operational maps, etc.<br>

							GIS is useful to create fertility, weed and pest intensity maps, which can then be used for making maps that show 
							recommended application rates of nutrients or pesticides.<br><br>

							<strong>Other Technologies</strong><br><br>
							<strong><i>Computer and Internet</i></strong><br>

							The computers and Internet are the most important component in enabling the precision farming possible as they are main
							source of information processing and gathering. The high-speed computer has made faster processing the data gathered 
							during precise management of the land parcel.<br>

							Internet, which is a network of computers, is the most recent development among all these technologies. The Internet has 
							bridged the gap between the information provider and the user. In agriculture, like any other form of business, internet
							has the capability to supply timely data about changing conditions.<br><br>

							<strong><i>Spatial Decision Support Systems (SDSS)</i></strong><br>

							SDSS are designed to help growers to solve complex spatial problems and to make decision concerning to irrigation scheduling,
							fertilisation, use of crop growth regulators and other chemicals.<br>

							Spatial decision support systems have evolved in parallel with decision support systems. In addition, in order to effectively
							support decision-making for complex spatial problems, a SDSS will need to:<br>

								~ provide for spatial data input<br>
								~ allow storage of complex structures common in spatial data<br>
								~ include analytical techniques that are unique to spatial analysis<br>
								~ provide output in the form of maps and other spatial forms<br><br>	
							
							<strong><i>Yield Mapping</i></strong><br>

							Yield mapping and soil sampling tend to be the first stage in implementing PF. Yield maps are produced by processing data 
							from an adapted combine that has a vehicle positioning system integrated with a yield recording system. Massey Ferguson 
							was the first company to produce a commercial yield mapping combine.<br>

							This combine has a differential GPS fitted to it that can be identified by the GPS receiver on the roof of the cab and 
							the differential aerial above the engine.<br>

							The output from the combine is a data file that recorded every 1.2 seconds the position of the combine in longitude and 
							latitude, with the yield at that point. This data set can then be processed by various geo-statistical techniques into 
							a yield map.<br><br>

							<strong><i>Diagnosis and recommendation integrated system (DRIS)</i></strong><br>

							DRIS represents a holistic approach to the mineral nutrition of crop and has an impact on the integrated set of norms 
							representing calibration of plant tissues, soil composition, environmental parameters and farming practices as the 
							functions of the yield of a crop.<br>

							Once such norms are developed, it is possible to make a diagnosis of the conditions of the crop thereby isolating the
							factors, which are likely to be responsible for limiting the growth and production.<br>

							The most important advantages of DRIS approach are its ability to make a diagnosis at any stage of crop development and
							to list the nutrient in the order of importance, which are responsible for limiting the yield.<br><br>

							<strong>Future strategy </strong><br><br>

							Future strategy for adoption of precision agriculture in India should consider the problem of land fragmentation, lack 
							of highly sophisticated technical centers for precision agriculture, specific software for precision agriculture, poor 
							economic condition of the farmers, etc.<br>

							Precision agriculture in small farms is that individual farms will be treated as if they were management zones within a
							field and that some centralised entity will provide information to the individual farmers on a co-operative basis.<br><br>

							The problem of high cost of positioning system for small fields can be solved by ‘dead reckoning system’. The dead reckoning
							system, suitable for small regularly shaped fields, relies on in-field markers, such as foam to maintain consistent application.
							This approach provided farmers with a robust and credible method for making decisions about spatial management of their fields.<br>

							Nature of crop and weed vary from zone to zone, country to country. So development of software and hardware for crop and 
							weeds of India, site specific tillage technique should be initiated.<br>

							If applied properly these will, then, be used for precision agriculture not only suitable for developed countries but also for
							developing countries. It has a wider impact in farm management through more efficient machinery management.<br>




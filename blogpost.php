            <p>50 percent of the red onions in Kenya are imported from Tanzania, as 
			indicated by Food and Agriculture Organisation's (FAO) 2014 report. 
			Kenyan Farmers have been doing their best to address the demand and close the gap, 
			but there is still more supply to be achieved. This makes the Red Bulb Onion
			a very attractive commercial investment for the Kenyan market at the moment, since local production is not enough.</p>
			
            <p>Major types of onions farmed in Kenya are bulb onions and spring onions. 
			The best areas suited for farming being Karatina, Oloitoktok, Naivasha, Kieni, Emali and Mai Mahiu </p>
			
			<p> Bulb onions take 3 to 4 months to reach maturity. Short rains are great for bulb onions since 
			they can be harvested between January and February. Local farmers are able to benefit during this time period,
			since there is low supply of onions from Tanzania at this time of the year. After the long rains of March, 
			the price of these onions hike and farmers who harvest after these period make good profit margins. </p>
			
			<p>Our recent market analysis, shows red onion prices in Mombasa are going at Ksh 1,174 per 15 kg bag while 
			the highest prices are in Eldoret, at Ksh 1,491 per 15 kg bag. Heavy rains experienced in the months of May and June
			in these areas have contributed to the price hikes. </p>
			
			<p>Spring onions are easy to plant. You can even plant them at your kitchen garden. 
			They are commonly used in vegetable salads or as seasoning in soups. 
			Some of the health benefits of spring onions include lower blood sugar and decreasing blood pressure and cholesterol levels. </p>
			
			<p>Of the two types, bulb onions are more popular than the spring ones due to their long shelf life and sweet taste.
			Currently, there are two different varieties of bulb onions in the market. There is the small, thin and firmly
			layered onion that has a strong pungent smell from Tanzania. The second one is the big loosely held variety that 
			is grown locally, in Kenya. </p>
			
			<p><strong>Conditions for Onion Farming </strong><br>

						Onions perform well in well drained, fertile, sandy loam, non-compacted soils.
						The ideal pH is 5.8 to 6.8. Onion farming is a worthy venture since it’s possible to
						grow onions throughout the year via irrigation. Soil test with accredited laboratories is advised. </p>

					<p>	The ideal temperatures onions need to grow are between 13 – 35 degrees centigrade. 
						This means that onions can grow in most parts of Kenya. However, to maximize production in areas 
						such as Ukambani and some parts in the Coastal area, one needs to invest in a greenhouse and drip irrigation.
						This is because greenhouses will optimize the temperatures since these areas are hot and irrigation will 
						provide the much needed water. Onions are a cool season crop. Most areas in Ukambani and the Coastal region are extremely dry.</p>

					<p>	In terms of requirements, apart from land that is an obvious fact, other requirements are DAP Fertilizer and seedlings.
						1 kg of seedlings can be used in 1 acre while 20 grams of DAP Fertilizer is applied per square meter.<br>

						You should note that there are different seed varieties and they perform differently under different conditions.
						Common hybrid varieties available in Kenya include Red Creole, Red Pinnoy, Jambar 1 and Red Bombay.
						The most popular seed variety in Kenya is the Jambar F1 since it produces high yield and the size of its onions are bigger.</p>

					<p>	<strong>Challenges facing Onion Farming in Kenya</strong> <br><br>

						<strong> <i>Rains or Excess Watering:</i></strong> Too much rains or excess watering can water log the crop. 
						If the rain in your area is too much, focus on growing the crop during the short rains. 
						You can also raise the beds to ensure the water flows out of the field. Avoid excessive watering of your onions.
						The best type of irrigation on onions is drip irrigation. Overhead irrigation should be avoided as it causes fungal diseases.<br><br>

						<strong> <i>Diseases:</i></strong>  Common diseases that affect onions include Downy Mildew, Bacterial Soft Rots, Pink/White Root,
						Botrytis, and Rusts. Use herbicides to get rid of the diseases.<br><br>

						<strong> <i>Pests: </i></strong> Common pests that attack onions include maggots, thrips, nematodes and the leaf miner.
						Use pesticides from accredited brands to get rid of the pests.<br><br>

						<strong> <i>Weeds:</i> </strong> Weeds can also destroy your crop and should be removed occasionally either physically or 
						through the use of herbicides. In one season, there should be at least 2 – 3 weeding sessions. <br><br>

						<strong>Harvesting onions.</strong> <br><br>

						The best time to do the harvesting is during the dry season. Keep a calendar and record the essential dates to ensure you
						harvest at the right time. Bulb onions are ready for harvesting once they form a shiny membranous cover around the bulbs
						or when the foliage withers. Spring onions are ready for harvesting when they are 15 centimeters tall and 1.5 centimeters thick.<br>

						Harvesting is done by pulling the bulbs and then chopping off the leaves. You then dry the bulbs in the sun before storing the produce.<br><br>

						Nairobi is experiencing the lowest prices at the moment at 825 kshs per 15 kg bag. This can be attributed to Nairobi being 
						the capital city and many farmers who produce the vegetable are in areas closely bordering it, such as Karatina, Mai Mahiu, Emali 
						and Kieni having easy access to the market. Get more market analysis from our Price trends to always get updated on the Onion trends. </p> <br>

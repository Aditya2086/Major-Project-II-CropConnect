			<p> Soil health has been defined as the "the continued capacity of soil to function as a vital living system,
						within ecosystem and land-use boundaries, to sustain biological productivity, 
						promote the quality of air and water environments, and maintain plant, animal, 
						and human health" (Pankhurst et al., 1997). <br> <br>

						Two elements in this definition of soil health distinguish it from the definition of soil quality: <br>
						(i) the inclusion of a time component (e.g. ''the continued capacity of'' - reflecting the 
						importance of the soil in being able to continue to function over time); and 
						(ii) recognition of soil "as a vital living system" (emphasizing the importance of the soil biota to soil functioning).<br> <br>

						Building on this definition of Pankhurst and co-authors, members of an international workshop at FAO,
						have come up with this definition:
						"Soil health is the capacity of soil to function as a living system, with ecosystem and land use boundaries,
						to sustain plant and animal productivity, maintain or enhance water and air quality, and promote plant and
						animal health. Healthy soils maintain a diverse community of soil organisms that help to control plant disease, 
						insect and weed pests, form beneficial symbiotic associations with plant roots; recycle essential plant nutrients;
						improve soil structure with positive repercussions for soil water and nutrient holding capacity,
						and ultimately improve crop production" (FAO, 2008). To this definition one might want to add an
						ecosystem perspective:  A healthy soil does not pollute its environment and does contribute
						to mitigating climate change by maintaining or increasing its carbon content.<br> <br>

						The concept of soil health captures the ecological attributes of the soil, 
						which have implications beyond its quality or capacity to produce a particular crop. 
						These attributes are chiefly those associated with the soil biota; its biodiversity,
						its food web structure, its activity and the range of functions it performs. For example, 
						soil biodiversity is not necessarily a soil property that is critical 
						for the production of a given crop, but it is a property that may be important for 
						the continued capacity of the soil to produce that crop.</p> <br>
			

			<p> 
								It's the warm season. Farmers are happy that their tomatoes are finally going to receive the much 
								sunlight needed for the fruit to grow bigger, juicier and firmer. What most probably don't know is 
								that with the warm weather sets in the most dreaded disease. 
								The monster that can wipe a farmer's field clean :<strong> Bacteria Wilt.</strong> <br> <br>

								Bacteria Wilt is caused by the pathogen bacterium Ralstonia Solanacearum and is quite 
								common in the moist & humid sandy soils. This bacterium lives in the soil and will work 
								its way quickly through the roots and up the stem of the plants. 
								It's spread by contaminated equipment, soil, water and infected plant material. <br> <br>

								<strong>Symptoms</strong> <br>

								The first symptoms of the disease is wilting of the youngest lower leaves of the plant
								and eventually the entire plant dies without any leaf spotting or yellowing. 
								These symptoms usually occur when the weather is hot, the humidity is high or the soil is wet.
								It is also common with soils that have high pH. <br> <br>

								<strong>The glass test.</strong> <br>

								You can test for bacterial wilt by cutting the stem at the base of the plant. 
								Look for discolored tissue. Suspend the stem in a glass of water.
								If it is infected, a white, slimy substance will ooze into the water.<br><br>

								<strong>Treatment & Control</strong> <br>

								There are no known chemicals to effectively control this disease. 
								it is a good recommendation that planting resistant varieties is probably the best solution so far.

								So how do I prevent Bacterial Wilt asks the farmer? Follow these good cultural controls says M-Farm:<br>
								

									~ Test your soil for bacterial wilt before planting. There are several institutions that
									Ensure to also test for pH and maintain a pH of 6.2-6.5 <br>

									~ Rotate your crops regularly <br>

									~ If any of your crops is affected, uproot them.<br> 

									~ Growing the plants off the ground in bags is recommended. The bags should not 
									directly sit on the ground to avoid any contact with the soil.
									If not planting in bags or pots,try raised beds to improve drainage as running water spreads the disease fast<br>

									~ Space plants far enough apart to provide good air circulation.<br>

									~ Wash your hands after handling infected plants and sterilizes any gardening tool 
									that could have been used in infected soil.<br>
								


								With these few tips and tricks, we wish all the best in maintaining a disease free farm. </p><br>

			
